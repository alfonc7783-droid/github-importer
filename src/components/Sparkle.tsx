/**
 * =============================================================================
 * КОМПОНЕНТ БЛЁСТКИ (SPARKLE)
 * =============================================================================
 * 
 * Файл: src/components/Sparkle.tsx
 * Описание: Анимированная блёстка (звёздочка) для декоративного оформления
 * 
 * ИСПОЛЬЗОВАНИЕ:
 * ──────────────
 * <Sparkle top="20%" left="10%" size="lg" delay={0.5} />
 * 
 * РАЗМЕРЫ:
 * ────────
 * - 'sm' — 8x8px (w-2 h-2)
 * - 'md' — 12x12px (w-3 h-3)
 * - 'lg' — 16x16px (w-4 h-4)
 * 
 * АНИМАЦИЯ:
 * ─────────
 * Использует animate-sparkle из index.css:
 * - Пульсация opacity (0.3 → 1 → 0.3)
 * - Масштабирование (0.8 → 1.2 → 0.8)
 * - Цикл 2 секунды
 * 
 * SVG-ФОРМА:
 * ──────────
 * Четырёхконечная звезда с острыми лучами.
 * Белый цвет с тенью для объёма.
 * 
 * @see src/components/SunburstBackground.tsx - Использование компонента
 * @see src/index.css - Определение animate-sparkle
 */

// ═══════════════════════════════════════════════════════════════════════════
// ТИПЫ
// ═══════════════════════════════════════════════════════════════════════════

interface SparkleProps {
  /** Позиция сверху (CSS значение, например '20%') */
  top: string;
  /** Позиция слева (опционально) */
  left?: string;
  /** Позиция справа (опционально) */
  right?: string;
  /** Размер блёстки: 'sm' | 'md' | 'lg' */
  size: 'sm' | 'md' | 'lg';
  /** Задержка начала анимации (секунды) */
  delay: number;
}

// ═══════════════════════════════════════════════════════════════════════════
// КОНСТАНТЫ
// ═══════════════════════════════════════════════════════════════════════════

/**
 * Маппинг размеров на Tailwind-классы
 */
const sizeMap = {
  sm: 'w-2 h-2',   // 8x8px
  md: 'w-3 h-3',   // 12x12px
  lg: 'w-4 h-4',   // 16x16px
};

// ═══════════════════════════════════════════════════════════════════════════
// КОМПОНЕНТ
// ═══════════════════════════════════════════════════════════════════════════

/**
 * Анимированная блёстка
 * 
 * @param top - Позиция сверху
 * @param left - Позиция слева (или right)
 * @param right - Позиция справа (или left)
 * @param size - Размер: 'sm' | 'md' | 'lg'
 * @param delay - Задержка анимации в секундах
 */
const Sparkle = ({ top, left, right, size, delay }: SparkleProps) => {
  return (
    <div
      className={`absolute ${sizeMap[size]} animate-sparkle`}
      style={{
        top,
        left,
        right,
        animationDelay: `${delay}s`,
      }}
    >
      {/* SVG четырёхконечной звезды */}
      <svg viewBox="0 0 24 24" fill="white" className="w-full h-full drop-shadow-lg">
        <path d="M12 0L14 10L24 12L14 14L12 24L10 14L0 12L10 10L12 0Z" />
      </svg>
    </div>
  );
};

export default Sparkle;
