/**
 * =============================================================================
 * ФОН С РАДИАЛЬНЫМИ ЛУЧАМИ (SUNBURST)
 * =============================================================================
 * 
 * Файл: src/components/SunburstBackground.tsx
 * Описание: Декоративный фон для стартового экрана с лучами и блёстками
 * 
 * ВИЗУАЛЬНЫЙ ЭФФЕКТ:
 * ──────────────────
 * - Радиальные лучи чередующихся оттенков розового
 * - Анимированные блёстки (sparkles) в разных точках экрана
 * 
 * ЦВЕТА ЛУЧЕЙ:
 * ────────────
 * - Светлый: hsl(350 55% 85%) — светло-розовый
 * - Тёмный: hsl(350 45% 75%) — более насыщенный розовый
 * - Чередуются каждые 10 градусов
 * 
 * БЛЁСТКИ:
 * ────────
 * Расположены в 8 точках экрана с разными размерами и задержками анимации.
 * Используют компонент Sparkle для рендеринга SVG-звёздочек.
 * 
 * @see src/components/Sparkle.tsx - Компонент блёстки
 * @see src/pages/Index.tsx - Использование на стартовом экране
 */
import Sparkle from './Sparkle';

// ═══════════════════════════════════════════════════════════════════════════
// КОНФИГУРАЦИЯ БЛЁСТОК
// ═══════════════════════════════════════════════════════════════════════════

/**
 * Позиции и параметры блёсток
 * 
 * @property top - Позиция сверху (CSS %)
 * @property left/right - Позиция слева/справа (CSS %)
 * @property size - Размер: 'sm' | 'md' | 'lg'
 * @property delay - Задержка анимации (секунды)
 */
const sparkles = [
  { top: '15%', left: '20%', size: 'lg' as const, delay: 0 },
  { top: '25%', right: '15%', size: 'md' as const, delay: 0.5 },
  { top: '60%', left: '10%', size: 'md' as const, delay: 0.3 },
  { top: '75%', right: '20%', size: 'lg' as const, delay: 0.7 },
  { top: '45%', left: '5%', size: 'sm' as const, delay: 0.2 },
  { top: '80%', left: '25%', size: 'sm' as const, delay: 0.4 },
  { top: '20%', right: '25%', size: 'sm' as const, delay: 0.6 },
  { top: '70%', right: '10%', size: 'md' as const, delay: 0.1 },
];

// ═══════════════════════════════════════════════════════════════════════════
// КОМПОНЕНТ
// ═══════════════════════════════════════════════════════════════════════════

/**
 * Фон с радиальными лучами и блёстками
 * Используется на стартовом экране (Index page)
 */
const SunburstBackground = () => {
  return (
    <>
      {/* ═══════════════════════════════════════════════════════════════ */}
      {/* РАДИАЛЬНЫЕ ЛУЧИ */}
      {/* repeating-conic-gradient создаёт чередующиеся секторы */}
      {/* ═══════════════════════════════════════════════════════════════ */}
      <div
        className="absolute inset-0"
        style={{
          background: 'repeating-conic-gradient(hsl(350 55% 85%) 0deg, hsl(350 55% 85%) 10deg, hsl(350 45% 75%) 10deg, hsl(350 45% 75%) 20deg)',
        }}
      />
      
      {/* ═══════════════════════════════════════════════════════════════ */}
      {/* БЛЁСТКИ */}
      {/* Анимированные звёздочки в разных точках экрана */}
      {/* ═══════════════════════════════════════════════════════════════ */}
      {sparkles.map((sparkle, index) => (
        <Sparkle key={index} {...sparkle} />
      ))}
    </>
  );
};

export default SunburstBackground;
